const groupBy = (items, key) =>
  items.reduce(
    (result, item) => ({
      ...result,
      [item[key]]: [...(result[item[key]] || []), item],
    }),
    {},
  );

let fs = require("fs");

buildings = buildings
  .filter((building) => !building.image.endsWith("back.png"))
  .map((building) => {
    let name = building.name
      .split(" ")
      .slice(1, -2)
      .map((w, i) => (i !== 0 && (w === "of" || w === "the") ? w : w.charAt(0).toUpperCase() + w.slice(1)));

    return {
      id: building.id,
      name: name.join(" "),
      game: "fh",
      image: building.image.replaceAll(".png", ".jpeg"),
      imageBack: building.image.replaceAll(".png", "-back.jpeg"),
    };
  })
  .sort((a, b) => a.id > b.id);
buildings = groupBy(buildings, "id");
buildings = Object.keys(buildings)
  .sort((a, b) => a.id > b.id)
  .map((key) => ({
    id: buildings[key][0].id,
    name: buildings[key][0].name,
    game: buildings[key][0].game,
    image: buildings[key].map((x) => x.image),
    imageBack: buildings[key].map((x) => x.imageBack),
  }));

fs.writeFile("building-cards.js", JSON.stringify(groupBy(buildings, "game")), "utf8", () =>
  console.log("buildings", buildings.length),
);
