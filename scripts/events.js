const groupBy = (items, key) =>
  items.reduce(
    (result, item) => ({
      ...result,
      [item[key]]: [...(result[item[key]] || []), item],
    }),
    {}
  );

let fs = require("fs");

events = events
  .filter((event) => event.name.endsWith("f"))
  .map((event) => {
    let expansion = event.expansion;
    switch (event.expansion) {
      case "Gloomhaven":
        expansion = "gh";
        break;
      case "Forgotten Circles":
        expansion = "gh";
        break;
      case "Jaws Of The Lion":
        expansion = "jotl";
        break;
      case "Crimson Scales":
        expansion = "cs";
        break;
      case "Frosthaven":
        expansion = "fh";
        break;
      case "Trail of Ashes":
        expansion = "toa";
        break;
    }

    let eventType;
    let season;
    switch (event.name.split("-")[1]) {
      case "be":
        eventType = "boat";
        break;
      case "ce":
        eventType = "city";
        break;
      case "rf":
        eventType = "rift";
        break;
      case "soe":
        eventType = "outpost";
        season = "summer"
        break;
      case "woe":
        eventType = "outpost";
        season = "winter"
        break;
      case "re":
        eventType = "road";
        break;
      case "sre":
        eventType = "road";
        season = "summer"
        break;
      case "wre":
        eventType = "road";
        season = "winter"
        break;
    }

    return {
      name: parseInt(event.name.split("-")[2]),
      game: expansion,
      image: event.image.replaceAll(".png", ".jpeg"),
      imageBack: event.image.replaceAll("f.png", "b.jpeg"),
      eventType: eventType,
      ...(season && { season: season }),
    };
  });

fs.writeFile(
  "event-cards.js",
  JSON.stringify(groupBy(events, "game")),
  "utf8",
  () => console.log("events", events.length)
);
